            <div class="font-justify">

                <p>La Creación de la Universidad en Puno es producto de la exigencia de intelectuales y políticos. La
                    sociedad
                    puneña considerada cuna de los intelectuales más brillantes del Perú. Los diputados Ramos, Aguirre y
                    Quiñones fueron autores de este proyecto de Ley de creación (RAMOS A. 1987: 21 -25).
                </p>
                <p>En el año de 1856, la convención Nacional dictaminó la Ley No. 406 de creación de la Universidad de
                    Puno,
                    promulgada el 23 de agosto de 1856 y firmada el 29 de agosto del mismo año, por el Presidente de la
                    República Don Ramón Castilla. La Ley No. 406 contiene 04 artículos: el primero más resaltante señala
                    que,
                    «Se erige en la ciudad de Puno una Universidad, para la enseñanza de la Teología, Jurisprudencia,
                    Medicina,
                    Filosofía y Letras, Matemáticas y Ciencias Naturales y para que en estas mismas Facultades se
                    confieran
                    Grados Académicos». El segundo dispone: «La Universidad observará el Reglamento de Instrucción
                    Pública
                    del
                    07 de abril de 1855». Como sucede con toda institución que nace, no se contaba con Estatuto propio,
                    local
                    necesario y la respectiva asignación presupuestal. El artículo tercero indica que, «se regirá por el
                    Estatuto de la Universidad San Agustín de Arequipa, y que sus funciones universitarias se celebrarán
                    en
                    el
                    Colegio Nacional de Puno». Asimismo, dispone que los gastos que originen las Cátedras de esta
                    novísima
                    Universidad serán afectados a las rentas del Colegio.

                </p>
                <p>Las razones por las cuales la universidad dejó de funcionar se relacionan con problemas
                    socio-políticos y
                    económicos. Otra razón que determinó su cierre oficial es el hecho de que no fue considerada en el
                    Reglamento Nacional de Instrucción Pública de 1876.

                </p>
                <h2 class="color-blue py-4">Reapertura</h2>
                <p>La reapertura de la Universidad es producto de varios proyectos de Ley. Destaca el primer proyecto de
                    Ley
                    presentado el 02 de noviembre de 1954 por el Dr. José Antonio Encinas, en su condición de Senador
                    por
                    Puno,
                    proyecto que contenía 03 artículos. El Proyecto propone que se organizara la Universidad en
                    Facultades,
                    Institutos y Escuelas.

                </p>
                <p>El siguiente proyecto de Ley fue presentado el 15 de noviembre de 1955 por el Senador Carlos A.
                    Barreda
                    Ramos. Este proyecto que contenía 04 artículos proponía la creación de las Facultades de Agronomía,
                    Veterinaria, Química Industrial y de las secciones de Pedagogía, Letras, Ciencias Económicas y
                    Comerciales y
                    Bellas Artes.

                </p>
                <p>El 20 de noviembre de 1957, los diputados Teófilo Monroy Solórzano, Fernando Manrique Enríquez y
                    Roger
                    Cáceres Velásquez presentaron ante su respectiva Cámara un nuevo proyecto compuesto de 14 artículos,
                    en
                    los
                    que se proponía la instalación de 02 Facultades: Facultad de Estudios Agropecuarios y la Facultad de
                    Estudios Sociales.

                </p>
                <p>Los tres proyectos de Ley presentados no tuvieron la acogida necesaria. En ese entonces, imperaba un
                    gobierno
                    dictatorial y no le convenía crear y reaperturar más universidades. Por cuanto, estas se constituían
                    en
                    un
                    peligro para su gobierno.

                </p>

                <p>l día 15 de noviembre de 1958, el Senador Enrique Torres Belón, presenta en su Cámara el histórico
                    Proyecto
                    de Ley de Reapertura de la Universidad de Puno. Proyecto que contenía 23 artículos y proponía como
                    nombre
                    UNIVERSIDAD TÉCNICA DEL ALTIPLANO, que debía iniciar su funcionamiento con la sola Facultad de
                    INGENIERÍA
                    AGROPECUARIA. Este proyecto, luego de una brillante exposición y sustentación. Fue aprobado por
                    unanimidad
                    en la Cámara de Senadores. Sin embargo, como lo tipifica RAMOS, A. (1987: 55): «la noche triste del
                    04
                    de
                    noviembre de 1960», fecha que estaba determinada para su discusión y aprobación, no fue tratada ni
                    menos
                    aprobada, causa que se le imputa al diputado Roger Cáceres Velásquez y otros que habían boicoteado
                    nuestra
                    justa aspiración, a quienes el pueblo de Puno los declaró como traidores.


                </p>


                <p>A raíz de este problema, el pueblo de Puno fue convocado a una reunión de emergencia, que se realizó
                    el
                    26 de
                    noviembre de 1960, en la que a la renuncia irrevocable del presidente del Comité Pro Reapertura de
                    la
                    Universidad de Puno, fue elegido como presidente el Dr. Juan Zea Gonzáles y se cambió la
                    denominación a
                    Frente Departamental Pro Universidad de Puno.

                </p>

                <p>En la primera sesión de la Legislatura Extraordinaria, de la noche del 27 de diciembre de 1960, fue
                    aprobado
                    el proyecto de Ley propuesto por el Senador Enrique Torres Belón. En esta sesión 82 diputados
                    votaron a
                    favor y 10 en contra.

                </p>
                <p>Finalmente, después de dos meses, el 10 de febrero de 1961 fue promulgada la Ley No. 13516 que
                    reabría la
                    Universidad con el nombre de Universidad Técnica del Altiplano, siendo Presidente Constitucional de
                    la
                    República el Dr. Manuel Prado y Ugarteche. Este proyecto se había aprobado tal como fuera presentado
                    inicialmente, con sus 23 artículos, sin que se alterara ni una coma; conforme era el deseo de su
                    autor,
                    quien dijera «Yo soy responsable de los defectos o las virtudes de dicha Ley».

                </p>
                <p>El 29 de abril de 1962 se inició la actividad académica de la Universidad Técnica del Altiplano, en
                    las
                    aulas
                    aún no concluidas, debido a la demora en la entrega de la Quinta Valderrama por el Ministerio de
                    Guerra.
                    Se
                    inicia con la Facultad de Ingeniería Agropecuaria y el Instituto de Estudios Socio Económicos, en
                    ese
                    entonces no se había considerado a la escuela de Medicina Humana, fue por gestión del Dr. Julio
                    Bustinza,
                    Rector de ese entonces la apertura de la Facultad de medicina mediante Resolución Rectoral N° 300-81
                    del
                    29
                    de abril de 1981 entrando posteriormente en funcionamiento a partir del año 1982 siendo su primer
                    Decano
                    el
                    Dr. David Frisancho Pineda, inicialmente se le consideraba como Programa Académico de Medicina
                    Humana,
                    en el
                    año 1984 se lleva a cabo la creación de la Carrera Profesional de Nutrición Humana y junto a ella se
                    crea la
                    Facultad de Ciencias de Salud, al que posteriormente se adición la Carrera Profesional de
                    Odontología,
                    un 25
                    de enero de 1994 mediante Resolución Rectoral N°107-94-R-UNA

                </p>

                <p>El 11 de Julio de 1999 se publica la Ley N° 27154, Ley que institucionaliza la Acreditación de
                    Facultades
                    o
                    Escuelas de Medicina en las Universidades del País; crea a su vez la comisión para la acreditación
                    de
                    Facultades o Escuelas de Medicina Humana (CAFME) después de un proceso de autoevaluación,
                    presentación
                    de
